/**
 * This is a class to represent one bit.
 * @author Sheng hao Dong
 * @version 1.0
 */
public class bit {
	// A storage for a bit. 
	private int value; 
	
	// Getter method
	public int getValue()
	{
		return value;
	}
	
	// Setter method
	public void setValue(int newValue)
	{
		this.value = newValue;
	}
}
